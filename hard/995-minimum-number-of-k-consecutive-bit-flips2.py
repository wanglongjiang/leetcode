'''
K 连续位的最小翻转次数

在仅包含 0 和 1 的数组 A 中，一次 K 位翻转包括选择一个长度为 K 的（连续）子数组，同时将子数组中的每个 0 更改为 1，而每个 1 更改为 0。

返回所需的 K 位翻转的最小次数，以便数组没有值为 0 的元素。如果不可能，返回 -1。
'''
from typing import List
'''
思路：遍历数组A，每遇到1个0，执行一次K位翻转，直到剩余元素数<K，如果剩余元素没有0，返回翻转的次数，否则返回-1
对于A.length和K很大的情况下，上述算法会超出测试时间，可以不实际执行翻转，直接跳过K范围内开始的连续0
1、0数量，翻转
'''


class Solution:
    def minKBitFlips(self, A: List[int], K: int) -> int:
        hasZero = False
        length = len(A)
        flipNum = 0
        i = 0
        while i < length:
            if A[i] == 1:
                i += 1
                continue
            if i + K > length:
                i += 1
                hasZero = True
                continue
            flipNum += 1
            zeroCount = 1
            hasOne = False
            for j in range(K - 1):
                if A[i + j + 1] == 0:
                    if not hasOne:
                        zeroCount += 1
                        hasZero = False
                    else:
                        A[i + j + 1] = 1
                else:
                    hasOne = True
                    A[i + j + 1] = 0
                    hasZero = True
            i += zeroCount
        if hasZero:
            return -1
        else:
            return flipNum


