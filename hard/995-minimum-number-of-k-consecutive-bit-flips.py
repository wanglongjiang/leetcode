'''
K 连续位的最小翻转次数

在仅包含 0 和 1 的数组 A 中，一次 K 位翻转包括选择一个长度为 K 的（连续）子数组，同时将子数组中的每个 0 更改为 1，而每个 1 更改为 0。

返回所需的 K 位翻转的最小次数，以便数组没有值为 0 的元素。如果不可能，返回 -1。
'''
from typing import List
'''
思路：遍历数组A，每遇到1个0，执行一次K位翻转，直到剩余元素数<K，如果剩余元素没有0，返回翻转的次数，否则返回-1
'''


class Solution:
    def minKBitFlips(self, A: List[int], K: int) -> int:
        hasZero = False
        length = len(A)
        flipNum = 0
        for i in range(length):
            if A[i] == 1:
                continue
            hasZero = True
            if i + K > length:
                continue
            flipNum += 1
            for j in range(K):
                A[i + j] = 1 if A[i + j] == 0 else 0
            hasZero = False
        if hasZero:
            return -1
        else:
            return flipNum


